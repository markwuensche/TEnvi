def normalize_renderer_backend(params):
    """Normalize renderer backend setting to a supported canonical value."""

    backend = str(params.get('renderer_backend', 'software')).strip().lower()
    if backend not in {'software', 'moderngl'}:
        backend = 'software'
    params['renderer_backend'] = backend
    return backend

# Extra padding so the closest tunnel segment never intersects the near clipping
# plane. Without this offset, the recycle loop mode causes the front face to be
# exactly at z=0 which leads to white bars and z-fighting flicker. Using a
# slightly larger offset also stabilizes the geometry so the front-most segment
# does not jitter when it approaches the camera.
FRONT_OFFSET = 0.35
# Recycle a segment only after it is completely behind the camera.
# Waiting ensures the segment stays visible until its back end passes
# the viewer which avoids white flashes when recycling.

def generate_filename(params):
    """Create a descriptive file name from tunnel parameters.

    The previous implementation only used the resolution, which caused files
    generated by :mod:`batch_exporter` to overwrite each other.  The new naming
    scheme includes the tunnel dimensions, segment length, duration and speed so
    that each parameter combination results in a unique filename.
    """

    mode = 'bw' if params['color_mode'] == 'bw' else 'col'
    brightness = {
        'alternating': 'alt',
        'random': 'rnd',
        'monotonous': 'mono'
    }[params['segment_brightness']]
    markers = params['depth_markers']

    recycle = 'recy' if params.get('recycle_loop') else 'norecy'

    return (
        f"tunnel_{params['tunnel_width']:.1f}w_"
        f"{params['tunnel_height']:.1f}h_"
        f"{params['segment_length']:.1f}l_"
        f"{params['tunnel_depth']:.0f}d_"
        f"{params['duration']:.1f}s_"
        f"{params['speed']:.1f}spd_"
        f"{recycle}_"
        f"{mode}_{brightness}_{markers}.mp4"
    )


def export_video(params, output_dir=None):
    """Render a video with the exact pipeline used by the GUI export button.

    This ensures the same renderer initialization, background color, palette
    mapping, oversampling, and ffmpeg encoding are applied consistently across
    the GUI, :mod:`batch_exporter`, and :mod:`interactive_protocol` helpers.
    """

    working = dict(params)
    normalize_renderer_backend(working)
    validate_params(working)

    # GUI-only flags default to safe values so batch/CLI users get the same pipeline.
    working.setdefault('show_camera_marker', False)
    working.setdefault('motion_blur', False)
    working.setdefault('blur_samples', 5)

    renderer = create_renderer(working)
    filename = generate_filename(working)
    output_dir = output_dir or os.getcwd()
    os.makedirs(output_dir, exist_ok=True)
    final_path = os.path.join(output_dir, filename)

    width, height = compute_render_resolution(working)
    fps = float(working['fps'])
    total_frames = max(1, int(round(working['duration'] * fps)))

    oversample = max(1, int(working.get('temporal_oversample', 1)))
    blur_samples = int(working.get('blur_samples', 5)) if working.get('motion_blur') else 1
    combined_samples = max(1, oversample * max(1, blur_samples))
    frame_interval = 1.0 / fps

    ffmpeg_path = shutil.which("ffmpeg") or "C:\\ffmpeg\\bin\\ffmpeg.exe"
    if not os.path.exists(ffmpeg_path):
        raise FileNotFoundError("FFmpeg not found. Please install it or add it to your PATH.")

    ffmpeg_cmd = [
        ffmpeg_path,
        "-y",
        "-f", "rawvideo",
        "-pix_fmt", "bgr24",
        "-s", f"{width}x{height}",
        "-r", str(fps),
        "-i", "-",
        "-f", "lavfi", "-t", str(working['duration']),
        "-i", "anullsrc=channel_layout=stereo:sample_rate=44100",
        "-shortest",
        "-c:v", "libx264",
        "-preset", "slow",
        "-crf", "8",
        "-pix_fmt", "yuv420p",
        "-movflags", "+faststart",
        "-profile:v", "high",
        "-level", "4.2",
        "-c:a", "aac",
        "-b:a", "128k",
        final_path,
    ]

    process = subprocess.Popen(ffmpeg_cmd, stdin=subprocess.PIPE)

    try:
        for frame_index in range(total_frames):
            base_time = frame_index * frame_interval

            if combined_samples == 1:
                cam_z = working['speed'] * (base_time + 0.5 * frame_interval)
                frame = renderer.render_frame(cam_z)
            else:
                sub_dt = frame_interval / combined_samples
                blended = None
                for sample_idx in range(combined_samples):
                    sample_time = base_time + (sample_idx + 0.5) * sub_dt
                    cam_z = working['speed'] * sample_time
                    sub_frame = renderer.render_frame(cam_z).astype(np.float32)
                    blended = sub_frame if blended is None else blended + sub_frame
                frame = np.clip(blended / combined_samples, 0, 255).astype(np.uint8)

            if working.get('show_camera_marker'):
                center = (frame.shape[1] // 2, frame.shape[0] // 2)
                cv2.line(frame, (center[0] - 10, center[1]), (center[0] + 10, center[1]), (0, 0, 255), 2)
                cv2.line(frame, (center[0], center[1] - 10), (center[0], center[1] + 10), (0, 0, 255), 2)

            process.stdin.write(frame.tobytes())
    finally:
        process.stdin.close()
        process.wait()
        renderer.release()

    if process.returncode != 0:
        raise RuntimeError("FFmpeg failed to encode the video. Please check the console for details.")

    settings_path = final_path.replace('.mp4', '_settings.json')
    with open(settings_path, 'w', encoding='utf-8') as f:
        json.dump(working, f, indent=2)

    return final_path, settings_path

def apply_palette_color(params):
    """Synchronize ``color_rgb`` with the selected ``color_palette``.

    This mirrors the GUI dropdown mapping so previews, exports, and batch runs
    always use the exact same palette-derived RGB tuple without requiring
    callers to remember to update ``color_rgb`` manually.
    """

    palette = params.get('color_palette')
    if palette in PALETTE_RGB:
        params['color_rgb'] = PALETTE_RGB[palette]


